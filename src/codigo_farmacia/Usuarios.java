    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
       Connection conn=null;
        try{
            conn = conectar();
            ps = conn.prepareStatement("INSERT INTO usuarios(email,password,nombre,apellidos,direccion,ciudad,telefono,id_tipo)VALUES(?,?,?,?,?,?,?,?)");
            ps.setString(1, txtEmail.getText());
            ps.setString(2, txtPassword.getText());
            ps.setString(3, txtNombre.getText());
            ps.setString(4, txtApellidos.getText());
            ps.setString(5, txtDireccion.getText());
            ps.setString(6, txtCiudad.getText());
            ps.setString(7, txtTelefono.getText());
            ps.setInt(8, Integer.valueOf(txtTipo.getText()));
            int res = ps.executeUpdate ();
            if(res > 0){
                JOptionPane.showMessageDialog(null, "producto guardado");
                limpiar_cajas();
                consultar();
            }else{
                JOptionPane.showMessageDialog(null, "Error al guardar el producto");
                limpiar_cajas();
            }
            conn.close();
        }catch(Exception e){
            System.out.println(e);
        }      
    }//GEN-LAST:event_btnGuardarActionPerformed

