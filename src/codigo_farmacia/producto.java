    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        Connection conn=null;
        try{
            conn = conectar();
            ps = conn.prepareStatement("INSERT INTO productos(nombre,tipo,cantidad,descripcion,receta)VALUES(?,?,?,?,?)");
            ps.setString(1, txtnombre_p.getText());
            ps.setString(2, txttipo.getText());
            ps.setString(3, txtcantidad.getText());
            ps.setString(4, txtdescripcion.getText());
            ps.setInt(5, Integer.valueOf(txtreceta.getText()));
            int res = ps.executeUpdate ();
            if(res > 0){
                JOptionPane.showMessageDialog(null, "producto guardado");
                limpiar_cajas();
                consultar();
            }else{
                JOptionPane.showMessageDialog(null, "Error al guardar el producto");
                limpiar_cajas();
            }
            conn.close();
        }catch(Exception e){
            System.out.println(e);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

