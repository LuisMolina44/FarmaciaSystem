    private void jtProductosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtProductosMouseClicked
    int fila = jtProductos.getSelectedRow();
    if (fila== -1){
        JOptionPane.showMessageDialog(null,"No has seleccionado ningun producto");
    }else{
        id=Integer.parseInt((String)jtProductos.getValueAt(fila, 0).toString());
        String nombre=(String)jtProductos.getValueAt(fila, 1);
        String tipo=(String)jtProductos.getValueAt(fila, 2);
        int cantidad=Integer.parseInt((String)jtProductos.getValueAt(fila, 3).toString());
        String descripcion=(String)jtProductos.getValueAt(fila, 4);
        int receta=Integer.parseInt((String)jtProductos.getValueAt(fila, 5).toString());
        txtidd.setText(""+id);
        txtnombre_p.setText(nombre);
        txtcantidad.setText(""+cantidad);
        txtdescripcion.setText(descripcion);
        txtreceta.setText(""+receta);
    }
    }//GEN-LAST:event_jtProductosMouseClicked

